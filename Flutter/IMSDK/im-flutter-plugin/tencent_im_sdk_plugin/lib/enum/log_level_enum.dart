enum LogLevelEnum {
  /// 无日志
  ///
  V2TIM_LOG_NONE,

  /// dart枚举站位，不可使用
  ///
  V2TIM_NULL,

  /// 全量日志
  ///
  V2TIM_LOG_ALL,

  ///输出 DEBUG，INFO，WARNING，ERROR 级别的 log
  ///
  V2TIM_LOG_DEBUG,

  ///输出 INFO，WARNING，ERROR 级别的 log 3
  ///
  V2TIM_LOG_INFO,

  ///输出 WARNING，ERROR 级别的 log 4
  ///
  V2TIM_LOG_WARN,

  ///输出 ERROR 级别的 log 5
  V2TIM_LOG_ERROR
}
