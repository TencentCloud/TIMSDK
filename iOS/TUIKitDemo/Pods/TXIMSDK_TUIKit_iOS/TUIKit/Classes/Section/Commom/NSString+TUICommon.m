- (NSString *)substringAtRange:(NSRange)rang
{
    if ([NSString isEmpty:self])
    {
        return nil;
    }

    if (rang.location > self.length)
    {
        return nil;
    }

    if (rang.location + rang.length > self.length)
    {
        return nil;
    }

    return [self substringWithRange:rang];
}

